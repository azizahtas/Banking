    private void btn_DeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_DeleteActionPerformed
        getAllText();
        String sel="select * from Login where Password='"+Pass+"' and Access='"+Access+"'";
        String del="delete from Login where Username='"+User+"'";
        if (User.equals("Nothing Selected")) 
        {
          JOptionPane.showMessageDialog(null, "Please select a user form list.", "Exception", JOptionPane.ERROR_MESSAGE);  
        } 
        else if(Pass.equals(""))
        {
        JOptionPane.showMessageDialog(null, "Please enter admin level password", "Exception", JOptionPane.ERROR_MESSAGE); 
        }
        else
        {
            try {
                rs=conMe.getConnection(sel);
                boolean b=rs.next();
                
                if (b) 
                {
                    int i=conMe.UpdateInsertDeleteQuerry(del);
                    if (i==1) { JOptionPane.showMessageDialog(null, "Deletion Successfull", "Sucess", JOptionPane.INFORMATION_MESSAGE);
                    } else {JOptionPane.showMessageDialog(null, "Nothing Deleted", "Failure", JOptionPane.ERROR_MESSAGE);
                    }
                } else {JOptionPane.showMessageDialog(null, "Wrong Password or non Admin level Password", "Error", JOptionPane.ERROR_MESSAGE);
                }
            } 
            catch (SQLException ex) 
            {
                JOptionPane.showMessageDialog(null, "Exception Occured", "Exception", JOptionPane.ERROR_MESSAGE);
                
            }
            
        }

    }//GEN-LAST:event_btn_DeleteActionPerformed

