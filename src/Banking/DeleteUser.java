    private void getUsers()
    {
        
    String sel="select Username from Login where Access='U'";
    rs=conMe.getConnection(sel);
        try {
            while (rs.next()) 
            {            
           String s= rs.getString("Username");
              model.addElement(s);
            }
            lst_Users.setModel(model);
        } 
        catch (SQLException ex) 
        {
          JOptionPane.showMessageDialog(null, "Something Went Wrong", "Exception", JOptionPane.ERROR_MESSAGE);
        }
        
            
    
    }
    
