    private void btn_TransferActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_TransferActionPerformed
      getAllText();
      getFinalBalance();
        
        String insf="insert into balancesheet(Date,AccountNumber,Mode,Debit,Balance) values('"+CurrentDate+"','"+AccnoFrom+"','"+Mode+"','"+Amount+"','"+FinalBalanceFrom+"')";
        String inst="insert into balancesheet(Date,AccountNumber,Mode,Deposit,Balance) values('"+CurrentDate+"','"+AccnoTo+"','"+Mode+"','"+Amount+"','"+FinalBalanceTo+"')";
        if (Name.equals("")) 
        {
        JOptionPane.showMessageDialog(null, "Please select an account number from Drop Down list...","Error!!",JOptionPane.ERROR_MESSAGE);    
        } 
        else if(OldBalanceTo==-1)
        {
        JOptionPane.showMessageDialog(null, "Please select Destination Account from the Drop Down List","Error!!",JOptionPane.ERROR_MESSAGE);
        }
        else if(Amount==0) 
        {
        JOptionPane.showMessageDialog(null, "Please enter Amount","Error!!",JOptionPane.ERROR_MESSAGE);      
        }
        else if(Amount>OldBalance)
        {
        JOptionPane.showMessageDialog(null, "You cant Transfer more money than your account has..","Error!!",JOptionPane.ERROR_MESSAGE);
        }
        else
        {
        int a=conMe.UpdateInsertDeleteQuerry(insf);
        int b=conMe.UpdateInsertDeleteQuerry(inst);
            if (a==1&b==1) 
            {
                updateAccountBalance();
                JOptionPane.showMessageDialog(null, "Debited Successfully.", "Error", JOptionPane.INFORMATION_MESSAGE); 
                clearAll();
            } 
            else 
            {
            JOptionPane.showMessageDialog(null, "Something Went Wrong in Deposit Form.", "Error", JOptionPane.ERROR_MESSAGE); 
            } 
        
        }
    }//GEN-LAST:event_btn_TransferActionPerformed

