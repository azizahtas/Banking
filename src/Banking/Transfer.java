    private void fillData() 
    {

    CurrentDate=df.format(d1);
    txt_Date.setText(CurrentDate);
    
    String sele="select AccountNumber from customerinfo";
    rs=conMe.getConnection(sele);
        try {
            while(rs.next())
            {
                model.addElement(""+rs.getInt("AccountNumber"));
                model1.addElement(""+rs.getInt("AccountNumber"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(Widthdraw.class.getName()).log(Level.SEVERE, null, ex);
        }
        jcmb_AccnoFrom.setModel(model);
        AutoCompleteDecorator.decorate(jcmb_AccnoFrom);
        jcmb_AccnoTo.setModel(model1);
        AutoCompleteDecorator.decorate(jcmb_AccnoTo);
    }
    
    private void getAllText() 
    {
    CurrentDate=txt_Date.getText();
    Name=txt_Name.getText();
    Mode="Transfer";
    OldBalance=Integer.parseInt(txt_OldBal.getText());
    try{Amount=Integer.parseInt(txt_Amount.getText());}
    catch(NumberFormatException ex){JOptionPane.showMessageDialog(null, "Amount is not vaild\nPlease enter a valid amount...");}
    try{AccnoTo=Integer.parseInt((String)jcmb_AccnoTo.getSelectedItem());}
    catch(NumberFormatException ex){JOptionPane.showMessageDialog(null, "Please Enter Valid Account Number.", "Error", JOptionPane.ERROR_MESSAGE);}
    }

    private void getFinalBalance() {
        try{Amount=Integer.parseInt(txt_Amount.getText());}
        catch(NumberFormatException ex)
        {
        JOptionPane.showMessageDialog(null, "Please Enter Valid Amount.", "Error", JOptionPane.ERROR_MESSAGE);
        }
        OldBalance=Integer.parseInt(txt_OldBal.getText());
        FinalBalanceFrom=OldBalance-Amount;
        FinalBalanceTo=OldBalanceTo+Amount;
    }
    private void updateAccountBalance() 
    {
       String updf="update customerinfo set Balance='"+FinalBalanceFrom+"' where AccountNumber='"+AccnoFrom+"'";
       conMe.UpdateInsertDeleteQuerry(updf);
       String updt="update customerinfo set Balance='"+FinalBalanceTo+"' where AccountNumber='"+AccnoTo+"'";
       conMe.UpdateInsertDeleteQuerry(updt);
    }
    
    private void clearAll() 
    {
       txt_Name.setText("");
       txt_OldBal.setText("0");
       txt_Amount.setText("0");
    }
